let persone = 20;

// per incrementare una variabile
persone++;
persone += 1;
persone = persone + 1;

console.log(persone);

// quattro operazioni
persone -= 7;       //meno sette
persone *= 2;       // per due
persone /= 4;       // diviso quattro

console.log(persone);

// resto operatore '%'
let resto = persone % 3;
console.log(resto);


