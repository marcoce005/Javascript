const sum = (a, b) => { return a + b; }
const diff = (a, b) => { return a - b; }
const prod = (a, b) => { return a * b; }
const div = (a, b) => { return a / b; }

console.log(`3 + 4 = ${sum(3, 4)}`);
console.log(`3 - 4 = ${diff(3, 4)}`);
console.log(`3 * 4 = ${prod(3, 4)}`);
console.log(`3 : 4 = ${div(3, 4)}`);

